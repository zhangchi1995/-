/**
 * 长方形类
 * 两个变量:长和宽 并且实现get和set方法
 */
public class Rectangle {

    public int getWide() {
        return wide;
    }

    public void setWide(int wide) {
        this.wide = wide;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    int wide;
    int height;
}
